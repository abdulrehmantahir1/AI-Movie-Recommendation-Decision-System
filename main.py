    "| **How It Works Here** | Applies **genre, rating, year, and runtime** as strict filters. Any movie failing even one filter is removed. |\n",
    "| **Link to Other Stages** | Feeds a clean, genre-correct candidate pool into the **Heuristic** module. Prevents wrong-genre movies from ever reaching scoring. |\n",
