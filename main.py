    "            else:\n",
    "                score_label = f\"{Fore.WHITE}Partial Match{Style.RESET_ALL}\"\n",
    "\n",
    "            print(f\"\\n  {Style.BRIGHT}{rank:>2}. {title[:52]}{Style.RESET_ALL}\")\n",
    "            print(f\"      Genre    : {movie['genere']}{genre_tag}\")\n",
    "            print(f\"      Rating   : {movie['rating']}/10   \"\n",
    "                  f\"| AI Predicted: {rec['predicted_rating']}/10\")\n",
    "            print(f\"      Year     : {int(movie['release_year'])}   \"\n",
    "                  f\"| Runtime: {int(movie['runtime'])} min\")\n",
    "            print(f\"      Score    : {score:.1f}/100  ({score_label})\")\n",
    "\n",
    "            # Contextual explanation lines\n",
    "            reasons = []\n",
    "            if preferences and preferences.get('genres') and movie['genere'] in preferences['genres']:\n",
    "                reasons.append(f\"Matches your selected genre '{movie['genere']}'\")\n",
    "            if movie['rating'] >= 8.5:\n",
    "                reasons.append(\"Highly rated by audiences (8.5+)\")\n",
    "            if score >= 70:\n",
    "                reasons.append(\"Closely aligns with your year and runtime preferences\")\n",
    "\n",
    "            if reasons:\n",
    "                print(f\"      Why      : {' | '.join(reasons)}\")\n",
    "\n",
    "        print()\n",
    "\n",
    "\n",
    "# ──────────────────────────────────────────────────────────\n",
    "# INITIALIZE ALL MODULES\n",
    "# ──────────────────────────────────────────────────────────\n",
    "\n",
    "csp       = CSPModule(df)\n",
