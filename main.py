    "# Responsibility: Optimal final selection.\n",
    "# Uses a max-heap (priority queue) to efficiently extract\n",
    "# the top-N movies from the heuristic-ranked candidates.\n",
    "# Genre-matched movies receive an additional boost so they\n",
    "# always rank above equal-scored non-genre movies.\n",
    "# ══════════════════════════════════════════════════════════\n",
    "\n",
    "class AStarSearch:\n",
    "    \"\"\"\n",
    "    A* informed search over heuristic-scored candidates.\n",
    "    The heuristic score acts as the f(n) cost function.\n",
    "    A priority queue ensures we always expand the most\n",
    "    promising movie next, stopping once N results are found.\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, df, heuristic):\n",
    "        self.df        = df\n",
    "        self.heuristic = heuristic\n",
    "\n",
    "    def search(self, filtered_df, preferences, n=10):\n",
    "        print_section(\"A* Search — Selecting Best Matches\")\n",
    "\n",
    "        if filtered_df.empty:\n",
    "            print_error(\"No candidates available for A* search.\")\n",
    "            return []\n",
    "\n",
    "        candidates = filtered_df.copy()\n",
    "\n",
    "        # Compute A* cost (= heuristic score) for each candidate\n",
    "        candidates['a_star_score'] = candidates.apply(\n",
    "            lambda row: self.heuristic.calculate_score(row, preferences), axis=1\n",
    "        )\n",
    "\n",
    "        # Give an extra boost to exact genre matches so they\n",
    "        # always beat same-scored movies of a different genre.\n",
    "        if preferences.get('genres'):\n",
    "            genre_mask = candidates['genere'].isin(preferences['genres'])\n",
    "            candidates.loc[genre_mask, 'a_star_score'] += 10\n",
    "\n",
    "        # Build max-heap (Python's heapq is min-heap, so negate score)\n",
    "        heap = []\n",
    "        for idx, row in candidates.iterrows():\n",
    "            heapq.heappush(heap, (-row['a_star_score'], idx, row['movie_id']))\n",
    "\n",
    "        results    = []\n",
    "        seen_ids   = set()\n",
    "\n",
    "        while heap and len(results) < n:\n",
    "            neg_score, idx, movie_id = heapq.heappop(heap)\n",
    "\n",
    "            if movie_id in seen_ids:\n",
    "                continue\n",
    "            seen_ids.add(movie_id)\n",
    "\n",
    "            # Fetch the full movie record from the master dataframe\n",
    "            rows = self.df[self.df['movie_id'] == movie_id]\n",
    "            if rows.empty:\n",
    "                continue\n",
    "\n",
    "            results.append({\n",
    "                'movie': rows.iloc[0],\n",
    "                'score': -neg_score\n",
    "            })\n",
    "\n",
    "        print_success(f\"A* found {len(results)} recommendation(s)\")\n",
    "        return results\n",
    "\n",
    "\n",
    "# ══════════════════════════════════════════════════════════\n",
    "# PHASE 3 — K-MEANS CLUSTERING\n",
    "# ──────────────────────────────────────────────────────────\n",
    "# Groups movies into 8 clusters based on genre, year, and\n",
    "# runtime. Used by recommend_by_movie() to find movies that\n",
    "# are similar in overall profile to a reference film.\n",
    "# ══════════════════════════════════════════════════════════\n",
    "\n",
    "print_section(\"Building K-Means Clusters\")\n",
    "\n",
    "# One-hot encode genres so K-Means can use them numerically\n",
    "genre_dummies = pd.get_dummies(df['genere'])\n",
    "cluster_features = pd.concat([genre_dummies, df[['release_year', 'runtime']]], axis=1)\n",
    "\n",
    "# Standardize features so no single column dominates distance\n",
    "cluster_scaler         = StandardScaler()\n",
    "cluster_features_scaled = cluster_scaler.fit_transform(cluster_features)\n",
    "\n",
    "# Fit K-Means with 8 clusters (good balance for ~20 k movies)\n",
    "kmeans          = KMeans(n_clusters=8, random_state=42, n_init=10)\n",
    "df['cluster']   = kmeans.fit_predict(cluster_features_scaled)\n",
    "\n",
    "print_success(\"8 clusters created successfully\")\n",
    "for i in range(8):\n",
    "    count = len(df[df['cluster'] == i])\n",
    "    bar   = \"█\" * int(count / len(df) * 40)\n",
    "    print_info(f\"Cluster {i}: {count:>5,} movies  {bar}\")\n",
    "\n",
    "\n",
    "# ══════════════════════════════════════════════════════════\n",
    "# PHASE 4 — ANN RATING PREDICTOR\n",
    "# ──────────────────────────────────────────────────────────\n",
    "# A 3-layer MLP regressor trained to predict a movie's\n",
    "# expected rating given its genre, year, and runtime.\n",
    "# Predictions are shown alongside actual ratings in the\n",
    "# recommendation output as an AI quality estimate.\n",
    "# ══════════════════════════════════════════════════════════\n",
    "\n",
    "print_section(\"Training Neural Network (Rating Predictor)\")\n",
    "\n",
    "genre_dummies_ann  = pd.get_dummies(df['genere'])\n",
    "ann_features       = pd.concat([genre_dummies_ann, df[['release_year', 'runtime']]], axis=1)\n",
    "\n",
    "ann_scaler = StandardScaler()\n",
    "X          = ann_scaler.fit_transform(ann_features)\n",
    "y          = df['rating'].values\n",
    "\n",
    "X_train, X_test, y_train, y_test = train_test_split(\n",
    "    X, y, test_size=0.2, random_state=42\n",
    ")\n",
    "\n",
    "ann = MLPRegressor(\n",
    "    hidden_layer_sizes=(64, 32, 16),  # 3 hidden layers\n",
    "    activation='relu',\n",
    "    solver='adam',\n",
    "    max_iter=500,\n",
    "    random_state=42,\n",
    "    early_stopping=True,              # Stop if validation loss stops improving\n",
    "    verbose=False\n",
    ")\n",
    "\n",
    "ann.fit(X_train, y_train)\n",
    "y_pred = ann.predict(X_test)\n",
    "\n",
    "print_success(\"Neural network trained successfully\")\n",
    "print_info(f\"Mean Squared Error : {mean_squared_error(y_test, y_pred):.4f}\")\n",
    "print_info(f\"R² Score           : {r2_score(y_test, y_pred):.4f}\")\n",
    "\n",
    "\n",
    "def predict_rating(movie, ann_model, scaler_model, reference_df):\n",
    "    \"\"\"\n",
    "    Predict the rating of a single movie using the trained ANN.\n",
    "    Builds a feature vector matching the training format\n",
    "    (one-hot genre + release_year + runtime).\n",
    "    \"\"\"\n",
    "    genre_columns = pd.get_dummies(reference_df['genere']).columns\n",
    "\n",
    "    feature_vector = []\n",
    "    for genre in genre_columns:\n",
    "        feature_vector.append(1 if movie['genere'] == genre else 0)\n",
    "    feature_vector.append(movie['release_year'])\n",
    "    feature_vector.append(movie['runtime'])\n",
    "\n",
    "    X_single   = np.array(feature_vector).reshape(1, -1)\n",
    "    X_scaled   = scaler_model.transform(X_single)\n",
    "    prediction = ann_model.predict(X_scaled)[0]\n",
    "    return round(float(prediction), 1)\n",
    "\n",
    "\n",
    "# ══════════════════════════════════════════════════════════\n",
    "# RECOMMENDATION ENGINE\n",
    "# ──────────────────────────────────────────────────────────\n",
    "# Orchestrates the full pipeline:\n",
    "#   CSP filter → Heuristic rank → A* select → ANN predict\n",
    "# Exposes two public methods:\n",
    "#   recommend_by_preferences()  — user-defined criteria\n",
    "#   recommend_by_movie()        — find similar films\n",
    "# ══════════════════════════════════════════════════════════\n",
    "\n",
    "class RecommendationEngine:\n",
    "    \"\"\"\n",
    "    Central controller that wires all AI modules together\n",
    "    and produces the final ranked recommendation list.\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, df, csp, heuristic, search, kmeans_model, ann_model, scaler_model):\n",
    "        self.df        = df\n",
    "        self.csp       = csp\n",
    "        self.heuristic = heuristic\n",
    "        self.search    = search\n",
    "        self.kmeans    = kmeans_model\n",
    "        self.ann       = ann_model\n",
    "        self.scaler    = scaler_model\n",
    "\n",
    "    # ── Recommendation by user preferences ───────────────\n",
    "    def recommend_by_preferences(self, preferences, n=10):\n",
    "        print_header(\"GENERATING RECOMMENDATIONS\")\n",
    "\n",
    "        # Step 1: Hard filter via CSP\n",
    "        filtered = self.csp.filter(preferences)\n",
    "\n",
    "        if filtered.empty:\n",
    "            print_error(\"No movies match your criteria. Try broadening your filters.\")\n",
    "            return []\n",
    "\n",
    "        print_success(f\"{len(filtered):,} movies passed all filters\")\n",
    "\n",
    "        # Step 2: Soft rank via heuristic (keep top 5× n or 50 minimum)\n",
    "        pool_size = max(n * 5, 50)\n",
    "        ranked    = self.heuristic.rank(filtered, preferences, top_n=pool_size)\n",
    "        print_info(f\"Heuristic pool narrowed to: {len(ranked)} candidates\")\n",
    "\n",
    "        # Step 3: A* final selection\n",
    "        recommendations = self.search.search(ranked, preferences, n)\n",
    "\n",
    "        # Step 4: Attach ANN predictions and cluster info\n",
    "        for rec in recommendations:\n",
    "            rec['predicted_rating'] = predict_rating(\n",
    "                rec['movie'], self.ann, self.scaler, self.df\n",
    "            )\n",
    "            rec['cluster'] = rec['movie']['cluster']\n",
    "\n",
    "        return recommendations\n",
    "\n",
    "    # ── Recommendation by reference movie ────────────────\n",
    "    def recommend_by_movie(self, movie_name, n=10):\n",
    "        print_header(f\"FINDING MOVIES SIMILAR TO: {movie_name.upper()}\")\n",
    "\n",
    "        # Search for exact match first, then partial\n",
    "        match = self.df[self.df['title'].str.lower() == movie_name.lower()]\n",
    "        if match.empty:\n",
    "            match = self.df[\n",
    "                self.df['title'].str.lower().str.contains(movie_name.lower(), na=False)\n",
    "            ]\n",
    "\n",
    "        if match.empty:\n",
    "            print_error(f\"Movie '{movie_name}' not found in the dataset.\")\n",
    "            return []\n",
    "\n",
    "        source = match.iloc[0]\n",
    "        print_success(f\"Found: {source['title']}\")\n",
    "        print_info(f\"Genre   : {source['genere']}\")\n",
    "        print_info(f\"Rating  : {source['rating']}/10\")\n",
    "        print_info(f\"Year    : {int(source['release_year'])}\")\n",
    "\n",
    "        # Use the same cluster first for truly similar movies\n",
    "        pool = self.df[\n",
    "            (self.df['cluster'] == source['cluster']) &\n",
    "            (self.df['movie_id'] != source['movie_id'])\n",
    "        ]\n",
    "\n",
    "        # Widen to same genre if cluster pool is too small\n",
    "        if len(pool) < n * 2:\n",
    "            pool = self.df[\n",
    "                (self.df['genere']   == source['genere']) &\n",
    "                (self.df['movie_id'] != source['movie_id'])\n",
    "            ]\n",
    "\n",
    "        prefs = {\n",
    "            'genres'           : [source['genere']],\n",
    "            'min_rating'       : max(5.0, source['rating'] - 1.5),\n",
    "            'preferred_year'   : source['release_year'],\n",
    "            'preferred_runtime': source['runtime'],\n",
    "        }\n",
    "\n",
    "        ranked          = self.heuristic.rank(pool, prefs, top_n=max(n * 5, 50))\n",
    "        recommendations = self.search.search(ranked, prefs, n)\n",
    "\n",
    "        for rec in recommendations:\n",
    "            rec['predicted_rating'] = predict_rating(\n",
    "                rec['movie'], self.ann, self.scaler, self.df\n",
    "            )\n",
    "\n",
    "        return recommendations\n",
    "\n",
    "    # ── Display formatted results ─────────────────────────\n",
    "    def display(self, recommendations, preferences=None):\n",
    "        \"\"\"Pretty-print recommendation results to the terminal.\"\"\"\n",
    "        if not recommendations:\n",
    "            return\n",
    "\n",
    "        print_header(\"YOUR TOP RECOMMENDATIONS\")\n",
    "\n",
    "        for rank, rec in enumerate(recommendations[:10], start=1):\n",
    "            movie  = rec['movie']\n",
    "            title  = str(movie.get('title', f\"Movie #{movie.get('movie_id', rank)}\"))\n",
    "\n",
    "            # Show a tick/cross to confirm genre match\n",
    "            if preferences and preferences.get('genres'):\n",
    "                genre_tag = f\"  {Fore.GREEN}[✓ Match]{Style.RESET_ALL}\" \\\n",
    "                            if movie['genere'] in preferences['genres'] \\\n",
    "                            else f\"  {Fore.RED}[✗ Mismatch]{Style.RESET_ALL}\"\n",
    "            else:\n",
    "                genre_tag = \"\"\n",
    "\n",
    "            # Score label\n",
    "            score = rec['score']\n",
    "            if score >= 80:\n",
    "                score_label = f\"{Fore.GREEN}Excellent Match{Style.RESET_ALL}\"\n",
    "            elif score >= 60:\n",
    "                score_label = f\"{Fore.CYAN}Strong Match{Style.RESET_ALL}\"\n",
