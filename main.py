    "| **How It Works Here** | Assigns a **0–100 score** using: <br> – Genre: 40 pts <br> – Rating: 30 pts <br> – Year proximity: 15 pts <br> – Runtime: 10 pts <br> – Popularity: 5 pts |\n",
